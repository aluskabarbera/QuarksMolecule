CREATE TABLE NITROGENATED_BASES (
    NITROGENATED_BASE_ID NUMBER(1) GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    NITROGENATEDBASE_NAME VARCHAR2(255) NOT NULL,
    NITROGENATEDBASE_SYMBOL VARCHAR2(1) NOT NULL
);

ALTER TABLE NITROGENATED_BASES ADD CONSTRAINT nitrogenated_base_id_pk PRIMARY KEY (NITROGENATED_BASE_ID);

-- Clave foránea en Bases Nitrogenadas hacia Moleculas (1,1)
-- Una base nitrogenada solamente tiene una molecula y ella sola no crea ningun aminacido.
ALTER TABLE NITROGENATED_BASES ADD CONSTRAINT nitrogenated_bases_molecule_fk FOREIGN KEY (MOLECULE_ID) REFERENCES MOLECULES(MOLECULE_ID) ON DELETE CASCADE;

-- Clave foránea en Bases Nitrogenadas hacia Aminoacidos (1,N)
ALTER TABLE NITROGENATED_BASES ADD CONSTRAINT nitrogenated_bases_aminoacids_fk FOREIGN KEY (AMINOACID_ID) REFERENCES AMINOACIDS(AMINOACID_ID) ON DELETE CASCADE;

COMMENT ON TABLE NITROGENATED_BASES IS 'Nitrogenated Bases.';

